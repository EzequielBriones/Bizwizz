const leftArrow = document.querySelector(".left-arrow");
const rightArrow = document.querySelector(".right-arrow");
const slider = document.querySelector(".carousel__slider");
let cardIndex;
let slidenum;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////FUNCTIONS//////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////// ADDING RIGHT ARROW FUNCTIONALITY //////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////

rightArrow.addEventListener("click", function () {
  // Gets the value of the input
  cardIndex = document.querySelector('input[name="slide"]:checked').value;
  if (cardIndex < 3) {
    // If the value is less than the total, sums it to slide forward
    cardIndex++;
  } else {
    // If the value is the last slide, goes back to first slide
    cardIndex = 1;
  }
  slidenum = "slide" + cardIndex;
  document.getElementById(slidenum).checked = true;
});

/////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////// ADDING LEFT ARROW FUNCTIONALITY ///////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////

leftArrow.addEventListener("click", function () {
  // Gets the value of the input
  cardIndex = document.querySelector('input[name="slide"]:checked').value;
  if (cardIndex > 1) {
    // If the value is bigger than the total, subtracts it to slide backwards
    cardIndex--;
  } else {
    // If the value is the first slide, goes back to last slide
    cardIndex = 3;
  }
  slidenum = "slide" + cardIndex;
  document.getElementById(slidenum).checked = true;
});
